
// Example of Spread Operator

// Arrays
const arr1 = [1, 2, 3];
const arr2 = [4, 5, 6];
const combined = [...arr1, ...arr2];
console.log("Combined Array:", combined);

// Objects
const obj1 = { name: "Sammy", age: 19 };
const obj2 = { country: "Nigeria" };
const merged = { ...obj1, ...obj2 };
console.log("Merged Object:", merged);
